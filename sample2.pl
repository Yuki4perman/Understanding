#!/Strawberry/perl/bin
use strict;
use warnings;

# スカラ変数
my $name = "Alice";
my $age = 25;

print "Name: $name\n";
print "Age: $age\n";

# 配列
my @colors = ("Red", "Green", "Blue");
print "First color: $colors[0]\n";

# ハッシュ
my %person = ("name" => "Bob", "age" => 30);
print "Person's name: $person{name}\n";
print "Person's age: $person{age}\n";
